    private void btn_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registerActionPerformed
 String id = txt_id.getText();
String lastname = txt_lastname.getText();
String firstname = txt_firstname.getText();
String username = txt_username.getText();
String password = txt_password.getText();

if (id.isEmpty() || lastname.isEmpty() || firstname.isEmpty() || username.isEmpty() || password.isEmpty()) {
    JOptionPane.showMessageDialog(null, "All data is required");
} else {
    try {
        Class.forName("org.postgresql.Driver");
        Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/Userlogin", "admin", "110806");
        String sqlquery = "INSERT INTO login.login_create (id, lastname, firstname, username, userpassword) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sqlquery);
        
        
BigDecimal numericId = new BigDecimal(id); // Parse the string id to BigDecimal
pst.setBigDecimal(1, numericId); // Set the BigDecimal value to PreparedStatement


        pst.setString(2, lastname);
        pst.setString(3, firstname);
        pst.setString(4, username);
        pst.setString(5, password);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(null, "Registration complete");

        // Clear the text fields after successful registration
        txt_id.setText("");
        txt_lastname.setText("");
        txt_firstname.setText("");
        txt_username.setText("");
        txt_password.setText("");
tampiltabel();
        // Memanggil metode untuk menampilkan tabel setelah registrasi
       
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}


    }//GEN-LAST:event_btn_registerActionPerformed

