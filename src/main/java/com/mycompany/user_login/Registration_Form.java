    private void btn_checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_checkActionPerformed
        String username = txt_username.getText();
        try{
            Class.forName("org.postgresql.Driver");
            Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/Userlogin","admin","110806");
            String sqlquery = "SELECT * FROM login.login_create WHERE username ='"+username+"'";
            PreparedStatement pst = conn.prepareStatement(sqlquery);
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "username alredy exist");
                txt_username.setForeground(Color.red);
            }else{
                JOptionPane.showMessageDialog(null, "username is available");
                txt_username.setForeground(Color.black);
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_btn_checkActionPerformed

