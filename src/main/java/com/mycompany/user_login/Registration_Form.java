         public void tampiltabel(){
             DefaultTableModel tb = new DefaultTableModel();
        //judul kolom
        tb.addColumn("id");
        tb.addColumn("lastname");
        tb.addColumn("firstname");
        tb.addColumn("username");
        tb.addColumn("userpassword");
        jTable1.setModel(tb);
        
try{
    Statement stm = conn.createStatement();
        res = stm.executeQuery("SELECT * FROM login.login_create");
        while (res.next()){
            //mengambil record dari db lalu tampil
            tb.addRow(new Object[]{
            res.getString("id"),
            res.getString("lastname"),
            res.getString("firstname"),
            res.getString("username"),
            res.getString("userpassword"),
            });
            
        }
   }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Data Gagal Tampil"+ e.getMessage());
}
         }
         
